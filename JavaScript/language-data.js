let currentLang = 'en';

function t(key) {
    return translations[currentLang][key] || key;
}

function setLanguage(lang) {
    if (!translations[lang]) return;
    currentLang = lang;
    
    // Update static elements
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });

    // Update button text
    const langBtn = document.getElementById('lang-toggle');
    if(langBtn) langBtn.textContent = lang === 'en' ? 'VI' : 'EN';

    // Re-render dynamic components
    if (typeof initVehicleSpecCarousel === 'function') initVehicleSpecCarousel();
    if (typeof renderMegaMenuContent === 'function') renderMegaMenuContent();
}

// Initialize
document.addEventListener('DOMContentLoaded', () => {
    // Initialize default language on page load
    setLanguage('en');
    
    const langBtn = document.getElementById('lang-toggle');
    if(langBtn) {
        langBtn.addEventListener('click', (e) => {
            e.preventDefault();
            const newLang = currentLang === 'en' ? 'vi' : 'en';
            setLanguage(newLang);
        });
    }
});
